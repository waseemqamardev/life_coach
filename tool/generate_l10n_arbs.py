def main() -> None:
    en_data = json.loads(EN_FILE.read_text(encoding="utf-8"))
    string_keys = [k for k in en_data if not k.startswith("@") and k != "@@locale"]

    for locale, overrides in LOCALES.items():
        out: dict[str, object] = {"@@locale": locale}
        locale_trans = T.get(locale, {})
        for key in string_keys:
            if key in locale_trans:
                out[key] = locale_trans[key]
            else:
                out[key] = en_data[key]
        # copy metadata keys
        for key in en_data:
            if key.startswith("@") and key[1:] in out:
                out[key] = en_data[key]

        path = ROOT / f"app_{locale}.arb"
        path.write_text(
            json.dumps(out, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
        print(f"Wrote {path.name}")


if __name__ == "__main__":
    main()
